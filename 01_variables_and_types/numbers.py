# Python is completely object oriented, and not
# "statically typed". You do not need to declare variables
# before using them, or declare their type.
# Every variable in Python is an object.

# Integer
myint = 3
print(myint)

# Float (different notations)
myfloat = 3.4
print(myfloat)
myfloat = float(5)
print(myfloat)
